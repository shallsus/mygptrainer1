private void ShowFrame(int xPosition, int yPosition)
{
    // 콘솔 창의 크기를 확인하고, 충분히 큰 창 크기인지 확인하세요.
    Console.Clear();
    
    // x, y 위치 조정 (여유를 두고 테두리가 화면에 맞게 조정)
    xPosition -= 1;
    yPosition -= 1;

    Console.SetCursorPosition(xPosition, yPosition);
    Console.WriteLine("|-------------------------------------------------|");


    for (int i = 0; i < 10;i++)
    {
        yPosition++;
        Console.SetCursorPosition(xPosition, yPosition);
        Console.WriteLine("|                                                 |");
    }


    Console.SetCursorPosition(xPosition, yPosition);
    Console.WriteLine("|-------------------------------------------------|");
}

public void PrintTextInsideFrame(string[] text, int xPosition, int yPosition)
{
    ShowFrame(xPosition, yPosition);  

  
    int x = xPosition+1;  
    
    foreach (var line in text)
    {
 
        foreach (var c in line)
        {
            Console.SetCursorPosition(x, yPosition);
            Console.Write(c);  
            Thread.Sleep(10);  
            x = x+2;               
        }

        yPosition++;
        x = xPosition+1; 
    }
}

